
public class Idol {
		private String name;
		private String divisi;
		
		// employee name
		public Idol(String name, String divisi)
		{
			this.name = name;
			this.divisi = divisi;
		}
		
		public String getIdolName()
		{
			return name;
		}

		public String getIdolDivisi() {
			return divisi;
		}
}
