import java.util.*;

public class Group {
		
		private String name;
		private List<Idol> idol;
		
		public Group(String name, List<Idol> idol)
		{
			this.name = name;
			this.idol = idol;
		}
		
		public String getName() {
			return name;
		}
		
		public List<Idol> getTotal()
		{
			return idol;
		}
		
		public List<Idol> getIdol()
		{
			return idol;
		}
		
}
